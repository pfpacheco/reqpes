package br.senac.sp.reqpes.model;

public class CentroCusto {
	private String codSegmento;
	private String dscSegmento;
	
	public void setCodSegmento(String codSegmento) {
		this.codSegmento = codSegmento;
	}
	public String getCodSegmento() {
		return codSegmento;
	}
	public void setDscSegmento(String dscSegmento) {
		this.dscSegmento = dscSegmento;
	}
	public String getDscSegmento() {
		return dscSegmento;
	}
}